var app = angular.module('frontendApp');

app.factory('Member', ['$resource', 'apiRoot',
    function($resource, apiRoot){
        var result = $resource(apiRoot + 'members/:id', {id: '@id'},
        {
          'update': { method:'PUT' }
        });
        return result;
    }
]);

